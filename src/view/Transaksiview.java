    public void showTableKeranjang() {
        DefaultTableModel dtmKeranjang = new DefaultTableModel(new String[]{"Nama Produk", "Harga Satuan", "Jumlah"}, 0);
        dtmKeranjang.setRowCount(0);
        for (Transaksi_Pesan dp : this.arrKeranjang) {
            dtmKeranjang.addRow(new String[]{dp.getMenu().getNama_menu(), dp.getMenu().getHarga().toString(), dp.getJumlah_pesanan().toString()});
        }
        this.tblKeranjang.setModel(dtmKeranjang);
    }


    public void showComboBoxProduk() throws SQLException {
        DefaultComboBoxModel dcbmProduk = new DefaultComboBoxModel();
        for (Menu p : this.transaksi.getDataBarang()) {
            dcbmProduk.addElement(p.getNama_menu() + " (Kategori" + p.getKategori().getNama_Kategori() + ")");
        }
        this.cbProduk.removeAllItems();
        this.cbProduk.setModel(dcbmProduk);
    }

    public void showTotal() {
        this.tfTotal.setText(this.hargaTotal.toString());
    }

