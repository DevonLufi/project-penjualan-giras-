    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

        Transaksi_Pesan pemesanan = new Transaksi_Pesan();
        try {
            pemesanan.setTanggal_transaksi(new Date());
            pemesanan.setTotal(this.Total);
    
            this.transaksi.insertTransaksi(pemesanan);
            this.arrKeranjang = new ArrayList<>();
            this.showTableKeranjang();
            this.hargaTotal = 0.0;
            this.showTotal();
            this.showComboBoxProduk();

        } catch (SQLException err) {
            System.out.println(err);
        }


    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed

        try {

            Transaksi_Pesan dp = new Transaksi_Pesan();
            dp.setJumlah_pesanan(Integer.parseInt(tfJumlah.getText()));
            dp.setMenu(this.transaksi.getDataBarang().get(cbProduk.getSelectedIndex()));
            this.Total += this.transaksi.getDataBarang().get(cbProduk.getSelectedIndex()).getHarga() * Integer.parseInt(tfJumlah.getText());
          
            this.showTotal();
            this.arrKeranjang.add(dp);
            this.showTableKeranjang();
            this.tfJumlah.setText("");

        } catch (SQLException ex) {

            Logger.getLogger(Transaksiview.class.getName()).log(Level.SEVERE, null, ex);

        }

    }//GEN-LAST:event_btnTambahActionPerformed

